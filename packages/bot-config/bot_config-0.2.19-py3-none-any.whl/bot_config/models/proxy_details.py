# --------------------------------------------------------- class: ProxyDetails ---------------------------------------------------------- #

class ProxyDetails:

    # ------------------------------------------------------------- Init ------------------------------------------------------------- #

    def __init__(
        self,
        config_dict: dict
    ):
        self.host = config_dict['host']
        self.port = config_dict['port']

        self.string = '{}:{}'.format(self.host, self.port)


